#define LED_NUMBER	64
uint32_t color_buf[LED_NUMBER];
uint16_t pwmData[LED_NUMBER][24];

void WS2812_Send_buffer(uint32_t *color, uint32_t number_of_leds)
{
	for (int j=0; j<number_of_leds; j++) {
		for (int i=23; i>=0; i--)
		{
			if (color[j] & (1<<i))
			{
				pwmData[j][i] = 149;	// 66,6%
			}
			else {
				pwmData[j][i] = 74;		// 33,3%
			}
		}
	}

	HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t *)pwmData, 24 * number_of_leds);
	xSemaphoreTake(sem_t3, portMAX_DELAY);
}

void task3(void *param){
  /* Infinite loop */
  uint8_t angle = 0;
  const uint8_t angle_difference = 11;
  TickType_t last;

  sem_t3 = xSemaphoreCreateBinary();
  last = xTaskGetTickCount();
  while (1)
  {
	// Demo code for LED_NUMBER LEDs
	for(uint8_t i = 0; i < LED_NUMBER; i++) {
		// Calculate color
		color_buf[i] = hsl_to_rgb(angle + (i * angle_difference), 255, 127);
	}
	volatile TickType_t start = xTaskGetTickCount();
	WS2812_Send_buffer(color_buf, LED_NUMBER);
	volatile TickType_t finish = xTaskGetTickCount();
	(void)start;
	(void)finish;
	++angle;

	// Refresh 50 Hz
	vTaskDelayUntil(&last, 20);
  }
}


